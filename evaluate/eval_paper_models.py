"""Run CD/IR evaluation only on the 5 configs reported in the paper (Table 2).

Wraps eval_cd_ir.py, parses the JSON sidecar, and emits a Markdown summary.

Usage:
    # default: all 5 paper configs
    python evaluate/eval_paper_models.py --gt_pc_root data/cad_vec_pc

    # subset
    python evaluate/eval_paper_models.py --only shared_nomlp_144,baseline --gt_pc_root data/cad_vec_pc

    # self-contained (regenerate GT from gt_vec each time)
    python evaluate/eval_paper_models.py --gt_from_h5

KEEP IN SYNC WITH README Table 2.
"""
import argparse
import json
import os
import subprocess
import sys

PAPER_CONFIGS = [
    ("baseline",          "Baseline (Drawing2CAD)"),
    ("shared_decoder",    "Shared Decoder"),
    ("shared_nomlp_256",  "Shared + w/o MLP (256)"),
    ("shared_nomlp_192",  "Shared + w/o MLP (192)"),
    ("shared_nomlp_144",  "Shared + w/o MLP (144) [main]"),
]

REPO_ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
EVAL_SCRIPT = os.path.join(os.path.dirname(os.path.abspath(__file__)), "eval_cd_ir.py")


def settings_match(existing, intended):
    """True iff the comparable fields of two settings dicts agree."""
    keys = ["mode", "gt_pc_root", "normalize_gt", "n_points", "seed", "skip_list"]
    return all(existing.get(k) == intended.get(k) for k in keys)


def run_one(exp_name, args, intended_settings):
    src = os.path.join(args.proj_dir, exp_name, "test_results")
    output_dir = os.path.join(args.proj_dir, exp_name)
    output_txt = os.path.join(output_dir, "cd_ir.txt")
    output_json = output_txt + ".json"

    if not os.path.isdir(src):
        print(f"[eval_paper_models] SKIP {exp_name}: {src} not found "
              f"(run `python test.py --exp_name {exp_name} ...` first)")
        return ("missing", None)

    # Resume / skip if existing JSON matches intended settings
    if os.path.exists(output_json) and not args.force:
        try:
            with open(output_json) as jf:
                existing = json.load(jf)
            if settings_match(existing.get("settings", {}), intended_settings):
                print(f"[eval_paper_models] SKIP {exp_name}: {output_json} already up-to-date "
                      "(use --force to re-run)")
                return ("cached", existing)
            else:
                print(f"[eval_paper_models] {exp_name}: existing JSON has different settings, "
                      "re-running")
                if not args.allow_setting_mismatch:
                    print(f"[eval_paper_models] (will overwrite {output_json})")
        except Exception as e:
            print(f"[eval_paper_models] {exp_name}: failed to read {output_json} ({e}); re-running")

    # Build subprocess command
    cmd = [sys.executable, EVAL_SCRIPT,
           "--src", src,
           "--n_points", str(args.n_points),
           "--n_jobs", str(args.n_jobs),
           "--seed", str(args.seed),
           "--output", output_txt]
    if args.gt_from_h5:
        cmd.append("--gt_from_h5")
    else:
        cmd.extend(["--gt_pc_root", args.gt_pc_root])
    if args.normalize_gt:
        cmd.append("--normalize_gt")
    if args.timeout:
        cmd.extend(["--timeout", str(args.timeout)])
    if args.skip_list:
        cmd.extend(["--skip_list", args.skip_list])

    print(f"\n[eval_paper_models] === {exp_name} ===")
    print("  $ " + " ".join(cmd))
    # Stream stdout/stderr live (no capture) so OCC crashes are visible
    rc = subprocess.run(cmd, cwd=REPO_ROOT, check=False).returncode
    if rc != 0:
        print(f"[eval_paper_models] {exp_name}: eval_cd_ir.py exited with code {rc}")
        return ("failed", None)

    if not os.path.exists(output_json):
        print(f"[eval_paper_models] {exp_name}: expected {output_json}, not found")
        return ("failed", None)
    with open(output_json) as jf:
        result = json.load(jf)
    return ("ok", result)


def fmt_cd(v):
    return "N/A" if v is None else f"{v:.6f}"


def write_markdown(args, intended_settings, results):
    lines = []
    lines.append("# SwiftCAD CD / IR Results")
    lines.append("")
    lines.append("Generated by `evaluate/eval_paper_models.py`. ")
    lines.append(f"Mode: **{intended_settings['mode']}** "
                 f"({'precomputed GT pc — Drawing2CAD parity' if intended_settings['mode']=='precomputed' else 'GT regenerated from gt_vec — self-contained'})  ")
    lines.append(f"Normalization: **{'symmetric (both)' if intended_settings['normalize_gt'] else 'asymmetric (out_pc only)'}**  ")
    lines.append(f"n_points={intended_settings['n_points']}, seed={intended_settings['seed']}")
    if intended_settings["mode"] == "precomputed":
        lines.append(f"  gt_pc_root={intended_settings['gt_pc_root']}")
    lines.append("")
    lines.append("| Method | n_total | n_ok | n_pred_inv | n_gt_inv | IR (%) | CD mean | CD median | CD trim mean |")
    lines.append("|---|---:|---:|---:|---:|---:|---:|---:|---:|")
    for exp_name, label in PAPER_CONFIGS:
        if args.only and exp_name not in args.only:
            continue
        status, r = results.get(exp_name, ("missing", None))
        if status == "missing":
            lines.append(f"| {label} | _no test_results_ | | | | | | | |")
            continue
        if status == "failed":
            lines.append(f"| {label} | _eval failed_ | | | | | | | |")
            continue
        s = r["stats"]
        ir_pct = f"{100.0 * s['ir']:.2f}" if s["total"] else "N/A"
        lines.append(
            f"| {label} | {s['total']} | {s['n_ok']} | {s['n_pred_invalid']} | "
            f"{s['n_gt_invalid']} | {ir_pct} | {fmt_cd(s['cd_mean'])} | "
            f"{fmt_cd(s['cd_median'])} | {fmt_cd(s['cd_trimmed_mean'])} |"
        )
    lines.append("")
    lines.append(f"IR formula: `{next((r[1]['stats']['ir_formula'] for r in results.values() if r[1] is not None), 'n_pred_invalid / N')}`")
    lines.append("")
    return "\n".join(lines)


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--proj_dir", default="proj_log")

    gt_group = parser.add_mutually_exclusive_group(required=True)
    gt_group.add_argument("--gt_pc_root", type=str, default=None,
                          help="dir of precomputed <data_id>.h5.ply (Drawing2CAD parity)")
    gt_group.add_argument("--gt_from_h5", action="store_true",
                          help="regenerate GT pc from gt_vec on the fly")

    parser.add_argument("--normalize_gt", action="store_true", default=False)
    parser.add_argument("--n_points", type=int, default=2000)
    parser.add_argument("--n_jobs", type=int, default=8)
    parser.add_argument("--seed", type=int, default=0)
    parser.add_argument("--timeout", type=int, default=120)
    parser.add_argument("--skip_list", type=str, default=None,
                        help="forwarded to eval_cd_ir.py: file with one data_id per line to skip")
    parser.add_argument("--only", type=str, default=None,
                        help="comma-separated subset of exp_names to evaluate")
    parser.add_argument("--force", action="store_true",
                        help="re-run even if cd_ir.txt.json already exists with matching settings")
    parser.add_argument("--allow_setting_mismatch", action="store_true",
                        help="suppress warning when overwriting JSON with different settings")
    parser.add_argument("--output", type=str, default=None,
                        help="markdown summary path (default: <proj_dir>/cd_ir_summary.md)")
    args = parser.parse_args()

    if args.only:
        args.only = {x.strip() for x in args.only.split(",") if x.strip()}
        unknown = args.only - {n for n, _ in PAPER_CONFIGS}
        if unknown:
            print(f"[eval_paper_models] ERROR: unknown exp_names in --only: {sorted(unknown)}")
            print(f"[eval_paper_models] available: {[n for n, _ in PAPER_CONFIGS]}")
            sys.exit(2)
    else:
        args.only = None

    intended_settings = {
        "mode": "from_h5" if args.gt_from_h5 else "precomputed",
        "gt_pc_root": None if args.gt_from_h5 else os.path.normpath(args.gt_pc_root) if args.gt_pc_root else None,
        "normalize_gt": bool(args.normalize_gt),
        "n_points": args.n_points,
        "seed": args.seed,
        "skip_list": os.path.normpath(args.skip_list) if args.skip_list else None,
    }

    results = {}
    for exp_name, _label in PAPER_CONFIGS:
        if args.only and exp_name not in args.only:
            continue
        status, r = run_one(exp_name, args, intended_settings)
        results[exp_name] = (status, r)

    n_ok = sum(1 for s, _ in results.values() if s in ("ok", "cached"))
    if n_ok == 0:
        print("\n[eval_paper_models] ERROR: 0 configs evaluated. "
              "Run test.py for the relevant exp_names first, then re-invoke.")
        sys.exit(1)

    output_md = args.output or os.path.join(args.proj_dir, "cd_ir_summary.md")
    os.makedirs(os.path.dirname(output_md) or ".", exist_ok=True)
    md = write_markdown(args, intended_settings, results)
    with open(output_md, "w") as f:
        f.write(md)
    print(f"\n[eval_paper_models] wrote {output_md}")
    print("\n" + md)


if __name__ == "__main__":
    main()
